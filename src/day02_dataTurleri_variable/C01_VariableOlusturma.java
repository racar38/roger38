package day02_dataTurleri_variable;

public class C01_VariableOlusturma {

    public static void main(String[] args) {

        int sayi;

        // Deklarasyon SADECE bir kez yapılabilir.
        // ama deger atamasi istendigi kadar olabilir.


       // Bir Variable deger atanmadan da deklare edilebilir.
        // ancak deger atanmayan bir variable kullanilamaz.

        sayi = 20 ;

        System.out.println(sayi);  //18. satırda sayi variable değeri 20 dir.

        sayi = sayi + 10 ;

        System.out.println(sayi); //22.satırda sayi variable değeri 30 dur.

    }


}

